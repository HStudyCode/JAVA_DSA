package Shorts;
//Loops 
//Q)print number 0 to 10000;

public class Demo_6 {

    public static void main(String[] args) {


        //for loop
        for (int i = 0; i <= 10000; i++) {
            System.out.println(i);
        }


        //while loop
        int j=0;
        while (j<=100) {
            System.out.println(j);
            j++;
        }


        //do while loop
        int k=0;
        do {
            System.out.println(k);
            k++;
        } while (k<=10);
    }
}
